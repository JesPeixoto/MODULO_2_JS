//● Escreva um programa que use um loop while para contar de 10 até 1 e imprima cada número na tela, em ordem decrescente.

let contagem = 10; // inicio da contagem 
while (contagem >= 1) { // Inicia um loop while que continuará enquanto o valor da variável contagem for maior ou igual a 1
  console.log(contagem);
  contagem--; // Decrementa o valor da variável contagem em 1 a cada iteração do loop
}
